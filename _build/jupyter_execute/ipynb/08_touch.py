# ## touch コマンド
# - タイムスタンプを変更するコマンド

# ---
# `touch` *FILENAME*： *FILENAME*を作成する

# In[1]:


get_ipython().system('touch src/test.txt')
get_ipython().system('ls -la src/')


# ---
# `touch -m` *FILENAME*： *FILENAME*の内容を変更した時刻を変更する

# In[2]:


get_ipython().system('touch -m src/test.txt')
get_ipython().system('ls -la src/')


# ---
# `commands -m` *FILENAME*： *FILENAME*の内容を変更した時刻を変更する

# In[3]:


get_ipython().system('xx -l')


