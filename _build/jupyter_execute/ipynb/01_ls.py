# ## ls コマンド
# - list segmentsから名称がきている
# - fileやディレクトリの情報を表示する

# ---
# `ls`：ファイル一覧表示

# In[1]:


get_ipython().system('ls')


# ---
# `ls -a`：全てのディレクトリ表示

# In[2]:


get_ipython().system('ls -a')


# ---
# `ls -l`：詳細の表示

# In[3]:


get_ipython().system('ls -l')


# ---
# `ls -h`：容量をhuman-readableに表示する

# In[4]:


get_ipython().system('ls -lh')


# ---
# `ls -k`：容量をkb基準でに表示する

# In[5]:


get_ipython().system('ls -lk')


